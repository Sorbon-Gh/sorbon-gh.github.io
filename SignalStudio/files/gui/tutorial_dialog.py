def _chapter_card(num: int, title: str, shortcut: str, intro: str, sections: dict[str, str]) -> QFrame:
    # Карточка главы: заголовок + блоки с теорией и практическими пояснениями.
    frame = QFrame()
    frame.setObjectName('tutChapter')
    fl = QVBoxLayout(frame)
    fl.setSpacing(14)
    
    header_row = QHBoxLayout()
    num_lbl = QLabel(f'{num:02d}')
    num_lbl.setObjectName('chapNum')
    num_lbl.setFont(QFont('Segoe UI', 18, QFont.Weight.Bold))
    num_lbl.setFixedWidth(50)
    header_row.addWidget(num_lbl)
    
    title_col = QVBoxLayout()
    title_col.setSpacing(6)
    title_text = f'{title}  <span style="color: #8b949e; font-size: 9pt;">{shortcut}</span>' if shortcut else title
    title_lbl = QLabel(title_text)
    title_lbl.setObjectName('chapTitle')
    title_lbl.setFont(QFont('Segoe UI', 13, QFont.Weight.DemiBold))
    title_lbl.setTextFormat(Qt.TextFormat.RichText)
    title_col.addWidget(title_lbl)
    
    intro_lbl = QLabel(intro)
    intro_lbl.setStyleSheet('color: #8b949e; font-size: 9.5pt; font-style: italic;')
    intro_lbl.setWordWrap(True)
    intro_lbl.setAlignment(Qt.AlignmentFlag.AlignJustify)
    title_col.addWidget(intro_lbl)
    header_row.addLayout(title_col, 1)
    fl.addLayout(header_row)
    
    for sec_title, sec_text in sections.items():
        sec_box = QVBoxLayout()
        sec_box.setSpacing(6)
        sec_title_lbl = QLabel(f'<b>{sec_title}</b>')
        sec_title_lbl.setStyleSheet('color: #1890ff; font-size: 10pt;')
        sec_title_lbl.setTextFormat(Qt.TextFormat.RichText)
        sec_box.addWidget(sec_title_lbl)
        
        sec_body_lbl = QLabel(sec_text)
        sec_body_lbl.setObjectName('chapBody')
        sec_body_lbl.setTextFormat(Qt.TextFormat.RichText)
        sec_body_lbl.setWordWrap(True)
        sec_body_lbl.setAlignment(Qt.AlignmentFlag.AlignJustify)
        sec_body_lbl.setStyleSheet('color: #c9d1d9; font-size: 10pt; line-height: 150%;')
        sec_box.addWidget(sec_body_lbl)
        fl.addLayout(sec_box)
    
    return frame


